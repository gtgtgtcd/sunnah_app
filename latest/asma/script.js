function handleSearch(query) {
    const resultsContainer = document.getElementById('modalResultsBody');

    if (!query.trim()) {
        resultsContainer.innerHTML = `
            <div style="text-align:center; color:var(--text-muted); margin-top:50px; font-family:'Amiri';">
                <i class="fas fa-search" style="font-size: 2rem; opacity: 0.5; margin-bottom: 10px; display:block; margin-left: auto; margin-right: auto;"></i>
                ابدأ بكتابة اسم للبحث عنه
            </div>
        `;
        return;
    }

    // Use the Advanced Search Engine
    // This uses the Giant Index + Fuzzy Logic + Normalization
    let results = [];
    if (typeof SearchEngine !== 'undefined') {
        results = SearchEngine.search(query, allahNames);
    } else {
        // Fallback if engine fails to load
        results = allahNames.filter(name =>
            name.name.includes(query) ||
            name.transliteration.toLowerCase().includes(query.toLowerCase())
        );
    }

    if (results.length === 0) {
        resultsContainer.innerHTML = `
            <div style="text-align:center; color:var(--text-muted); margin-top:50px; font-family:'Amiri';">
                <i class="fas fa-exclamation-circle" style="font-size: 2rem; opacity: 0.5; margin-bottom: 10px; display:block;"></i>
                لم يتم العثور على نتائج
            </div>
        `;
        return;
    }

    resultsContainer.innerHTML = results.map(name => `
        <div class="search-result-item" onclick="selectNameFromSearch(${name.id - 1})">
            <div class="search-result-name">
                ${name.name} - ${name.transliteration}
                ${name.score && name.score < 85 ? `<span style="font-size:12px; color:var(--royal-gold); margin-right:5px;">(هل تقصد هذا؟)</span>` : ''}
            </div>
            <div class="search-result-meaning">${name.meaning}</div>
        </div>
    `).join('');
}

function selectNameFromSearch(index) {
    if (typeof displayName === 'function') {
        displayName(index);
    }
}

