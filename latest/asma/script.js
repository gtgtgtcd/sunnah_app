// ================= TOGGLE MEANING FUNCTION ================= //
function toggleMeaning() {
    const meaningEl = document.getElementById('nameMeaning');
    const btnEl = document.getElementById('showMoreBtn');

    if (!meaningEl || !btnEl) return;

    const computed = window.getComputedStyle(meaningEl);
    const lineHeight = parseFloat(computed.lineHeight) || 24;
    const collapsedLines = 3;
    const collapsedHeight = Math.round(lineHeight * collapsedLines);

    meaningEl.style.position = 'relative';

    const isExpanded = meaningEl.dataset.state === 'expanded';

    if (!isExpanded) {
        meaningEl.dataset.state = 'expanded';
        meaningEl.classList.add('expanded');
        meaningEl.style.maxHeight = collapsedHeight + 'px';
        meaningEl.style.opacity = '0.85';
        void meaningEl.offsetHeight;
        meaningEl.style.maxHeight = meaningEl.scrollHeight + 'px';
        meaningEl.style.opacity = '1';
        btnEl.textContent = 'عرض أقل';
        meaningEl.classList.remove('has-gradient');
    } else {
        meaningEl.dataset.state = 'collapsed';
        meaningEl.classList.remove('expanded');
        meaningEl.style.maxHeight = meaningEl.scrollHeight + 'px';
        meaningEl.style.opacity = '0.9';
        void meaningEl.offsetHeight;
        meaningEl.style.maxHeight = collapsedHeight + 'px';
        meaningEl.style.opacity = '1';
        btnEl.textContent = 'عرض المزيد';
        meaningEl.classList.add('has-gradient');
    }
}

// ================= DAILY NAME IN HERO CARD ================= //
function setDailyName() {
    // Get day of year to have consistent daily name
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const diff = now - start;
    const oneDay = 1000 * 60 * 60 * 24;
    const dayOfYear = Math.floor(diff / oneDay);

    // Use modulo to get index (0-98 for 99 names)
    const dailyIndex = dayOfYear % allahNames.length;
    const dailyName = allahNames[dailyIndex];

    // Update hero card safely
    const arabicEl = document.getElementById('heroNameArabic');
    const englishEl = document.getElementById('heroNameEnglish');
    const meaningEl = document.getElementById('heroNameMeaning');

    if (arabicEl) arabicEl.textContent = dailyName.name;
    if (englishEl) englishEl.textContent = `${dailyName.transliteration} - ${dailyName.meaning}`;
    if (meaningEl) meaningEl.textContent = dailyName.explanation.substring(0, 120) + '...';
}

// Call setDailyName when document loads
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', setDailyName);
} else {
    setDailyName();
}
