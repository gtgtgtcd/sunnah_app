function getVerseLink(source) {
    if (!source) return null;
    try {
        // التحسين: استخراج الرقم الأول فقط في حالة الآيات المركبة (مثال: 1-2 تصبح 1)
        const match = source.match(/سورة\s+(.+?)\s+-\s+آية\s+([\d-]+)/);

        if (match) {
            let surahName = match[1].trim();
            // تنظيف إضافي لاسم السورة (إزالة مسافات غير مرئية أو رموز)
            surahName = surahName.replace(/[^\u0600-\u06FF\s]/g, '').trim();

            // parseInt للرقم (يجلب الرقم الأول فقط من "1-2")
            let ayahNum = parseInt(match[2].trim());

            // 1. محاولة إيجاد رقم السورة (Exact Match)
            let surahId = surahNamesMap.indexOf(surahName);

            // 2. Fuzzy Match (إزالة التشكيل والهمزات والتاء المربوطة والألف المقصورة)
            if (surahId === -1) {
                const cleanText = (text) => text.replace(/[إأآا]/g, "ا").replace(/ة/g, "ه").replace(/ى/g, "ي").trim();
                const normName = cleanText(surahName);
                surahId = surahNamesMap.findIndex(s => cleanText(s) === normName);
            }

            console.log(`Link Gen: Source="${source}" -> Name="${surahName}" -> ID=${surahId}, Ayah=${ayahNum}`);

            if (surahId !== -1) {
                // التأكد من أن الرابط نسبي ويعمل بشكل صحيح
                const link = `../quran/index.html?surah=${surahId}&ayah=${ayahNum}`;
                console.log("Generated Link:", link);
                return link;
            } else {
                console.error("Surah not found:", surahName);
            }
        }

    } catch (e) {
        console.error("Link Error:", e);
    }
    return null;
}

