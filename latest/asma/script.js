function filterByCategory(category, button) {
    // Update active tab
    document.querySelectorAll('.tab-btn').forEach(btn => btn.classList.remove('active'));
    button.classList.add('active');

    // Filter names
    if (category === 'all') {
        filteredNames = [...allahNames];
    } else {
        filteredNames = allahNames.filter(n => n.category === category);
    }

    // Re-render list
    renderNamesList(filteredNames);

    // Display first name from filtered list
    if (filteredNames.length > 0) {
        const firstIndex = allahNames.findIndex(n => n.id === filteredNames[0].id);
        displayName(firstIndex);
    }
}

